public class EvenOdd {
    public static void main(String[] args) {
        // Declare variables
        int numToExamine = 100;
        // If then statement
        if (numToExamine % 2 == 0)
            System.out.println("The number is an even number" );
        else
            System.out.println("The number is an odd number" );
    }
}